package com.example.java.katas.sumofoddnumbers;

/**
 * Based on Code-wars kata:
 * https://www.codewars.com/kata/55fd2d567d94ac3bc9000064/java
 */
public class SumOfOddNumbers {

    /**
     * Sum odd numbers from given row number
     * Based on explanation https://planetmath.org/sumofoddnumbers
     *
     * @param n the row value to be calculated
     * @return odd sum
     */
    public static int sumOfOddNumbers(final int n) {
        return n * n * n;
    }
}
